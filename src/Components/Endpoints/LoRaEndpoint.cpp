//------------------------------------------------------------------------------------------------
// Description:
//------------------------------------------------------------------------------------------------
bool Endpoints::CLoRaEndpoint::ScheduleSend([[maybe_unused]] CMessage const& message)
{
    // Forward the message pack to be sent on the socket
    return ScheduleSend(message.GetDestinationId(), message.GetPack());
}

