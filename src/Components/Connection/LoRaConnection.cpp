//------------------------------------------------------------------------------------------------
// Description:
//------------------------------------------------------------------------------------------------
bool Connection::CLoRa::Shutdown()
{    // Stop the worker thread from processing the connections
    {
        std::lock_guard<std::mutex> lock(m_mutex);
        m_terminate = true;
    }

    m_cv.notify_all();
    
    if (m_worker.joinable()) {
        m_worker.join();
    }
    
    return !m_worker.joinable();
}

